function setI18nLang(lang) {
  _i18nLang = lang === 'en' ? 'en' : 'zh';
  document.documentElement.lang = _i18nLang === 'en' ? 'en' : 'zh-CN';
}

function t(key, vars) {
  let s = (UI[_i18nLang] && UI[_i18nLang][key]) || UI.zh[key] || key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      s = s.replace(new RegExp('\\{' + k + '\\}', 'g'), v);
    });
  }
  return s;
}

function tf(key, vars) {
  return t(key, vars);
}

function localizeItem(item) {
  if (_i18nLang !== 'en' || typeof ITEMS_EN === 'undefined') return item;
  const en = ITEMS_EN[item.id];
  if (!en) return item;
  return { ...item, title: en.title || item.title, problem: en.problem || item.problem, tools: en.tools || item.tools, aiRole: en.aiRole || item.aiRole };
}

function localizeCategory(cat) {
  if (_i18nLang !== 'en') {
    if (cat.id === 'allbuiltin') return { ...cat, name: t('catAllBuiltin'), desc: t('catAllBuiltinDesc') };
    if (cat.id === 'myentry') return { ...cat, name: t('catMyEntry'), desc: t('catMyEntryDesc') };
    if (cat.id === 'custom') return { ...cat, name: t('catCustom'), desc: t('catCustomDesc') };
    return cat;
  }
  if (cat.id === 'allbuiltin') return { ...cat, name: t('catAllBuiltin'), desc: t('catAllBuiltinDesc'), items: cat.items.map(localizeItem) };
  if (cat.id === 'myentry') return { ...cat, name: t('catMyEntry'), desc: t('catMyEntryDesc'), items: cat.items.map(localizeItem) };
  if (cat.id === 'custom') return { ...cat, name: t('catCustom'), desc: t('catCustomDesc'), items: cat.items.map(localizeItem) };
  const meta = CAT_I18N[cat.id];
  return {
    ...cat,
    name: meta?.name || cat.name,
    desc: meta?.desc || cat.desc,
    items: cat.items.map(i => ({ ...localizeItem(i), _catName: meta?.name || i._catName })),
  };
}

