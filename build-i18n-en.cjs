const result = {};
let autoCount = 0;
let manualCount = 0;

for (const [title, subtitle] of allToolsMap) {
  const tr = T[title];
  if (tr) {
    result[title] = { title: tr.title, desc: tr.desc || subtitle };
    manualCount++;
  } else if (isEnglish(title)) {
    // 英文品牌名，title 不翻译，subtitle 需要翻译
    result[title] = { title: title, desc: subtitle };
    autoCount++;
  } else {
    // 没有手动翻译的中文标题，保留原文
    result[title] = { title: title, desc: subtitle };
    autoCount++;
  }
}

function isEnglish(text) {
  if (!text) return true;
  return /^[\x00-\x7F\s\-_.·/()（）:,+&]+$/.test(text);
}

